
public class Calculator 
{

	int ans=0;
	double div=0.0;
	int Add(int a,int b)
	{
		
		ans=a+b;
		return(ans);
		
	}
	
	int Sub(int a,int b)
	{
		ans=a-b;
		return(ans);
		
	}
	
	int Mul(int a,int b)
	{
		ans=a*b;
		return(ans);
		
	}
	
	double Div(int a,int b)
	{
		div=a/b;
		return(div);
		
	}
	
	
}
